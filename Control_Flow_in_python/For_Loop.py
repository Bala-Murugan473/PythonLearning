# for is used to iterate over the elements of a sequence or any other iterable objects

for x in range(10):
    # four space indent
    print(x)
    # 1 blank line after loop
else:
    print('break occurred')

# else is an optional block in for loop
# it will execute only if the break occurs.
# it can be used to check weather the loop iterated completely or break inbetween
# continue statement has no impact with else clause.
