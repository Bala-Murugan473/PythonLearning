# A set is an unordered collection with no duplicates
# Set object supports mathematical operations like union, intersection, difference and symmetric
# To create a set we use set() constructor, not {}

# KEY FEATURES OF SET:
# Unordered
# Unchangeable but can be added
# No duplicates
# Non indexed(since unordered)
# Non slicable(since unordered)
# Can contain any datatype
